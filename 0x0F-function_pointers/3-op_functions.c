#include "3-calc.h"
/**
* op_add - returns sum of 2 no
* @a: 1st no
* @b: 2nd no
* Return: sum
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - returns diffrence of 2 no
* @a: 1st no
* @b: 2nd no
* Return: diffrence
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - return product of 2 no
* @a: 1st no
* @b: 2nd no
* Return: product
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - returns division of 2 no
* @a: 1st no
* @b: 2nd no
* Return: division
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - returns modulus of 2 numbers
* @a: 1st no
* @b: 2nd no
* Return: the remainder of division of 2 no
*/
int op_mod(int a, int b)
{
	return (a % b);
}
